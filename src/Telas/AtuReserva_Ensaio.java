    private void jButtonAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAtualizarActionPerformed
        // TODO add your handling code here:
        
        try{
int Id_Reserva_Ens = Integer.parseInt(jTextId_Reserva_Ens.getText());
String valor_reserva_ens = jTextValor_Reserva_Ens.getText();
String data_reserva_ens = jTextData_Reserva_Ens.getText();
String hora_entrada_reserva_ens = jFormattedTextFieldHora_Entrada_Reserva_Ens.getText();
String hora_saida_reserva_ens = jFormattedTextFieldHora_saida_Reserva_Ens.getText();
Reserva_Ensaio reserva_ensaio = new Reserva_Ensaio(valor_reserva_ens, data_reserva_ens, hora_entrada_reserva_ens, hora_saida_reserva_ens);
ConexaoReserva_Ensaio conecta_Reserva_Ensaio = new ConexaoReserva_Ensaio();
conecta_Reserva_Ensaio.AtualizarReserva_Ensaio(reserva_ensaio, Id_Reserva_Ens);

jTextId_Reserva_Ens.setText("");
jTextValor_Reserva_Ens.setText("");
jTextData_Reserva_Ens.setText("");
jFormattedTextFieldHora_Entrada_Reserva_Ens.setText("");
jFormattedTextFieldHora_saida_Reserva_Ens.setText("");
jTextId_Reserva_Ens.requestFocus();
        

JOptionPane.showMessageDialog(null, "Dados atualizados com sucesso!","Aviso",
JOptionPane.INFORMATION_MESSAGE);
} catch(Exception ex){
JOptionPane.showMessageDialog(null, "Erro no banco de dados"+ex.getMessage(),"Aviso"
,JOptionPane.ERROR_MESSAGE);
}
        
    }//GEN-LAST:event_jButtonAtualizarActionPerformed

