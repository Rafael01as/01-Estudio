    private void jButtonBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonBuscarActionPerformed
        // TODO add your handling code here:
        
         try{
Reserva_Ensaio reserva_ensaio = new Reserva_Ensaio();
reserva_ensaio.setID_RESERVA_ENS(Integer.parseInt(jTextId_Reserva_Ens.getText()));
ConexaoReserva_Ensaio conecta = new ConexaoReserva_Ensaio();
reserva_ensaio = conecta.buscarreserva_ensaio(reserva_ensaio.getID_RESERVA_ENS());

if(reserva_ensaio.getID_RESERVA_ENS() != 0){
jTextValor_Reserva_Ens.setText(reserva_ensaio.getVALOR_RESERVA_ENS());
jTextData_Reserva_Ens.setText(reserva_ensaio.getDATA_RESERVA_ENS());
jFormattedTextFieldHora_Entrada_Reserva_Ens.setText(reserva_ensaio.getHORA_ENTRADA_RESERVA_ENS());
jFormattedTextFieldHora_saida_Reserva_Ens.setText(reserva_ensaio.getHORA_SAIDA_RESERVA_ENS());


jButtonAtualizar.setEnabled(true);
jButtonExcluir.setEnabled(true);
}else{
JOptionPane.showMessageDialog(null, "Cliente nÃ£o encontrado","Aviso",
JOptionPane.ERROR_MESSAGE);
}
}
catch(Exception ex){
JOptionPane.showMessageDialog(null, "Erro no banco de dados "+ex.getMessage(),
"Aviso",JOptionPane.ERROR_MESSAGE);
}
        
    }//GEN-LAST:event_jButtonBuscarActionPerformed

