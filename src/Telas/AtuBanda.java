    private void jButtonBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonBuscarActionPerformed
        
        // AÃ§Ã£o para buscar dados referente ao id
       
            try{
Banda banda = new Banda();
banda.setID_BAN(Integer.parseInt(jTextId_Ban.getText()));
ConexaoBanda conecta = new ConexaoBanda();
banda = conecta.buscarbanda(banda.getID_BAN());

if(banda.getID_BAN() != 0){
jTextNome_Ban.setText(banda.getNOME_BAN());
jTextFone_Ban.setText(banda.getFONE_BAN());
jTextEmail_Ban.setText(banda.getEMAIL_BAN());
jTextGenero_Ban.setText(banda.getGENERO_BAN());
jTextResponsavel_Ban.setText(banda.getRESPONSAVEL_BAN());
jTextRua_Ban.setText(banda.getRUA_BAN());
jTextNumero_Ban.setText(banda.getNUMERO_BAN());

jButtonAtualizar.setEnabled(true);
jButtonExcluir.setEnabled(true);
}else{
JOptionPane.showMessageDialog(null, "Cliente nÃ£o encontrado","Aviso",
JOptionPane.ERROR_MESSAGE);
}
}
catch(Exception ex){
JOptionPane.showMessageDialog(null, "Erro no banco de dados "+ex.getMessage(),
"Aviso",JOptionPane.ERROR_MESSAGE);
}
            
        
    }//GEN-LAST:event_jButtonBuscarActionPerformed

